/**
 * Get explanation for an error code
 */
export function getErrorExplanation(code: string): ErrorExplanation {
  return ERROR_EXPLANATIONS[code] || ERROR_EXPLANATIONS['DEFAULT'];
}

/**
 * Get impact color
 */
export function getImpactColor(impact: ErrorExplanation['impact']): string {
  switch (impact) {
    case 'critical': return '#dc2626';
    case 'medium': return '#ea580c';
    case 'low': return '#ca8a04';
  }
}

/**
 * Get impact label in Greek
 */
export function getImpactLabel(impact: ErrorExplanation['impact']): string {
  switch (impact) {
    case 'critical': return 'ΚΡΙΣΙΜΟ';
    case 'medium': return 'ΜΕΤΡΙΟ';
    case 'low': return 'ΧΑΜΗΛΟ';
  }
}
